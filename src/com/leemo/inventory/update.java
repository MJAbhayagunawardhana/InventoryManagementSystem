    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        String Search=pro_id.getText();
        PreparedStatement ps;
        ResultSet rs;
        String query = "SELECT * FROM `equipment` WHERE productID=?";
        try{
            ps = connection.getConnection().prepareStatement(query);
                ps.setString(1, Search);
                rs=ps.executeQuery();
                if(rs.next()){
                   cat.setText(rs.getString("Category"));
                   quan.setText(rs.getString("productQuantity"));
                   pri.setText(rs.getString("productPrice"));
                   des.setText(rs.getString("productDescription"));   
                }
        }
        catch (SQLException ex) {
                Logger.getLogger(update.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null,"Searched product does not exist");
            }
        
    }//GEN-LAST:event_searchActionPerformed

