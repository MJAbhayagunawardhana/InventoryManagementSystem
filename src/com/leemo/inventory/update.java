    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        String Prod_ID=pro_id.getText();
        
        PreparedStatement ps;
        String query;
        query = "DELETE FROM `equipment` WHERE productID=?";
        try{
            ps = connection.getConnection().prepareStatement(query);
            ps.setString(1,Prod_ID );
            if(ps.executeUpdate()>0)
            {
                 JOptionPane.showMessageDialog(null,"Product details are updating");
            }
        }
        catch (SQLException ex) {
                Logger.getLogger(update.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String Cat=cat.getText();
        String Quan=quan.getText();
        String Price=pri.getText();
        String Des=des.getText();
        
        PreparedStatement ps2;
        String query2;
        query2 ="INSERT INTO `equipment`(`productID`, `Category`, `productQuantity`, `productPrice`, `productDescription`) VALUES (?,?,?,?,?)";
        
        try{
                ps2 = connection.getConnection().prepareStatement(query2);
                ps2.setString(1,Prod_ID );
                ps2.setString(2,Cat);
                ps2.setString(3,Quan );
                ps2.setString(4,Price );
                ps2.setString(5,Des );
            
                if(ps2.executeUpdate()>0)
                {
                    JOptionPane.showMessageDialog(null,"Product successfully edited");
                }
            }
        catch (SQLException ex) {
            Logger.getLogger(update.class.getName()).log(Level.SEVERE, null, ex);
        }  
        dispose();
    }//GEN-LAST:event_editActionPerformed

